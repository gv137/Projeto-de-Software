package variaveis;

public class tipoDados {

	public static void main(String[] args) {
		 
		// TIPOS INTEIROS
		//byte = -128 -- 127
		byte numero = 127;
		
		 //short = -32.768 -- 32.767
		short n2 = 300;
		
		//int = -2.147.483.648 -- 2.147.483.647
		int idade = 25;
		
		//long 
		long n3 = 300;
		
		//TIPOS DECIMAIS
		
		//float = -3,4 * 10^38 -- 3,4 * 10^38
		float peso = 40.60f;
		
		// double = -1,8 * 10^308 -- 1,8 * 10^308
		double altura = 1.36; 
		
		//TIPO TEXTO
		//"" - string
		//'' - char
		
		char genero = 'F';
		String nome = "Gustavo";
		
		//TIPO BOOLEAN 
		boolean logico = true;
		
	}

}
